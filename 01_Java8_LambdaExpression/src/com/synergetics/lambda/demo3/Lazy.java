package com.synergetics.lambda.demo3;

public class Lazy 
{
	public static boolean isEven(int number) {
	    return number % 2 == 0;
	}

	public static int doubleIt(int number) {
	    return number * 2;
	}

	public static boolean isGreaterThan5(int number) {
	    return number > 5;
	}
}
